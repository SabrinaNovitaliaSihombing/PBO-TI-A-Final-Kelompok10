package entities;

public class Library {
    private String library;
    private Integer id;

    public Library() {
    }

    public String getLibrary() {
        return library;
    }

    public void setLibrary(final String library) {
        this.library = library;
    }

    public Integer getId() {
        return id;
    }

    public void setId(final Integer id) {
        this.id = id;
    }
}
